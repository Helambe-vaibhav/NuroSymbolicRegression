import math
import sympy as sp

VARIABLES = {'x','y',} #,  'z'

class Node:
    def __init__(self, value, left=None, right=None):
        self.value = value
        self.left = left
        self.right = right

    def is_constant(self):
        return isinstance(self.value, (int, float))

    def count_nodes(self):
        count = 1
        if self.left:
            count += self.left.count_nodes()
        if self.right:
            count += self.right.count_nodes()
        return count

    def depth(self):
        if not self.left and not self.right:
            return 0
        left_depth = self.left.depth() if self.left else 0
        right_depth = self.right.depth() if self.right else 0
        return 1 + max(left_depth, right_depth)


    def evaluate(self, x):
        try:
            if self.value in VARIABLES:
                return x[self.value]
            elif self.value == 'pi':
                return math.pi
            elif self.value == 'e':
                return math.e
            elif self.value == '+':
                return self.left.evaluate(x) + self.right.evaluate(x)
            elif self.value == '-':
                return self.left.evaluate(x) - self.right.evaluate(x)
            elif self.value == '*':
                return self.left.evaluate(x) * self.right.evaluate(x)
            elif self.value == '/':
                denom = self.right.evaluate(x)
                return self.left.evaluate(x) / denom if abs(denom) > 1e-6 else 1.0
            elif self.value == '^':
                base = self.left.evaluate(x)
                exp = self.right.evaluate(x)
                if isinstance(exp, (int, float)) and int(exp) == exp:
                    return math.copysign(math.pow(abs(base), exp), base)
                return math.pow(abs(base), exp)
            elif self.value == 'sin':
                return math.sin(self.left.evaluate(x))
            elif self.value == 'cos':
                return math.cos(self.left.evaluate(x))
            elif self.value == 'exp':
                return math.exp(min(self.left.evaluate(x), 20))
            elif self.value == 'log':
                val = self.left.evaluate(x)
                return math.log1p(abs(val))
            else:
                return float(self.value)
        except:
            return 1.0

    def __str__(self):
        if self.left and self.right:
            return f"({str(self.left)} {self.value} {str(self.right)})"
        elif self.left:
            return f"{self.value}({str(self.left)})"
        else:
            return str(self.value)

    # Optional: clean tree string for developers
    def to_string(self):
        return str(self)


    def to_sympy(self):
        if self.value in VARIABLES:
            return sp.Symbol(self.value)
        elif self.value == 'pi':
            return sp.pi
        elif self.value == 'e':
            return sp.E
        elif self.is_constant():
            return sp.Float(self.value)
        elif self.value == '+':
            return self.left.to_sympy() + self.right.to_sympy()
        elif self.value == '-':
            return self.left.to_sympy() - self.right.to_sympy()
        elif self.value == '*':
            return self.left.to_sympy() * self.right.to_sympy()
        elif self.value == '/':
            return self.left.to_sympy() / self.right.to_sympy()
        elif self.value == '^':
            return self.left.to_sympy() ** self.right.to_sympy()
        elif self.value == 'sin':
            return sp.sin(self.left.to_sympy())
        elif self.value == 'cos':
            return sp.cos(self.left.to_sympy())
        elif self.value == 'exp':
            return sp.exp(self.left.to_sympy())
        elif self.value == 'log':
            return sp.log(self.left.to_sympy())
        else:
            raise ValueError(f"Unknown node value: {self.value}")

    def copy(self):
        return Node(self.value,
                    self.left.copy().simplify() if self.left else None,
                    self.right.copy().simplify() if self.right else None).simplify()

    def simplify(self):
        if self.left:
            self.left = self.left.simplify()
        if self.right:
            self.right = self.right.simplify()

        # Constant folding
        if self.left and self.right and self.left.is_constant() and self.right.is_constant():
            try:
                return Node(self.evaluate({}))
            except:
                return self
        if self.left and not self.right and self.left.is_constant():
            try:
                return Node(self.evaluate({}))
            except:
                return self

        # Rule-based simplification
        if self.value == '+':
            if self.right and self.right.is_constant() and self.right.value == 0:
                return self.left
            if self.left and self.left.is_constant() and self.left.value == 0:
                return self.right

        if self.value == '-':
            if self.right and self.right.is_constant() and self.right.value == 0:
                return self.left

        if self.value == '*':
            if (self.left.is_constant() and self.left.value == 0) or (self.right.is_constant() and self.right.value == 0):
                return Node(0)
            if self.left.is_constant() and self.left.value == 1:
                return self.right
            if self.right.is_constant() and self.right.value == 1:
                return self.left

        if self.value == '/':
            if self.right.is_constant() and self.right.value == 1:
                return self.left

        if self.value == '^':
            if self.right.is_constant() and self.right.value == 1:
                return self.left
            if self.right.is_constant() and self.right.value == 0:
                return Node(1)

        return self

    def differentiate(self, var):
        if self.is_constant() or (self.value not in VARIABLES and not self.left):
            return Node(0).simplify()

        if self.value == var:
            return Node(1).simplify()

        if self.value == '+':
            return Node('+', self.left.differentiate(var), self.right.differentiate(var)).simplify()

        if self.value == '-':
            return Node('-', self.left.differentiate(var), self.right.differentiate(var)).simplify()

        if self.value == '*':
            return Node('+',
                        Node('*', self.left.differentiate(var), self.right.copy()),
                        Node('*', self.left.copy(), self.right.differentiate(var))).simplify()

        if self.value == '/':
            num = Node('-',
                       Node('*', self.left.differentiate(var), self.right.copy()),
                       Node('*', self.left.copy(), self.right.differentiate(var)))
            denom = Node('^', self.right.copy(), Node(2))
            return Node('/', num, denom).simplify()

        if self.value == '^':
            if self.right.is_constant():
                new_exp = Node('-', self.right.copy(), Node(1))
                return Node('*',
                            Node('*', self.right.copy(), Node('^', self.left.copy(), new_exp)),
                            self.left.differentiate(var)).simplify()
            else:
                u = self.left
                v = self.right
                return Node('*',
                            Node('^', u.copy(), v.copy()),
                            Node('+',
                                 Node('*', v.differentiate(var), Node('log', u.copy())),
                                 Node('*', v.copy(), Node('/', u.differentiate(var), u.copy())))).simplify()

        if self.value == 'sin':
            return Node('*', Node('cos', self.left.copy()), self.left.differentiate(var)).simplify()

        if self.value == 'cos':
            return Node('*', Node('*', Node(-1), Node('sin', self.left.copy())), self.left.differentiate(var)).simplify()

        if self.value == 'exp':
            return Node('*', Node('exp', self.left.copy()), self.left.differentiate(var)).simplify()

        if self.value == 'log':
            return Node('/', self.left.differentiate(var), self.left.copy()).simplify()

        return Node(0).simplify()

    def get_node_signatures(self):
        return self._collect_signatures(self)

    def _collect_signatures(self, node):
        if node is None:
            return set()
        sigs = {str(node)}  # Use `node.label`, `node.operator`, etc. as needed
        for child in getattr(node, 'children', []):
            sigs |= self._collect_signatures(child)
        return sigs

    def __eq__(self, other):
        if not isinstance(other, Node):
            return False
        return str(self) == str(other)

    def __hash__(self):
        return hash(str(self))


