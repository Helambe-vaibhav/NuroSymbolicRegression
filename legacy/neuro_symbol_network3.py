# Expression Node
class Node:
    def __init__(self, value, left=None, right=None):
        self.value = value
        self.left = left
        self.right = right

    def evaluate(self, x):
        try:
            if self.value in VARIABLES:
                return x[self.value]
            elif self.value == '+':
                return self.left.evaluate(x) + self.right.evaluate(x)
            elif self.value == '-':
                return self.left.evaluate(x) - self.right.evaluate(x)
            elif self.value == '*':
                return self.left.evaluate(x) * self.right.evaluate(x)
            elif self.value == '/':
                denom = self.right.evaluate(x)
                return self.left.evaluate(x) / denom if abs(denom) > 1e-6 else 1.0
            elif self.value == '^':
                base = self.left.evaluate(x)
                exp = self.right.evaluate(x)
                return math.pow(abs(base), exp)
            elif self.value == 'sin':
                return math.sin(self.left.evaluate(x))
            elif self.value == 'cos':
                return math.cos(self.left.evaluate(x))
            elif self.value == 'exp':
                return math.exp(min(self.left.evaluate(x), 20))
            elif self.value == 'log':
                val = self.left.evaluate(x)
                return math.log1p(abs(val))
            else:
                return float(self.value)
        except:
            return 1.0

    def to_string(self):
        if self.value in VARIABLES or isinstance(self.value, (int, float)):
            return str(self.value)
        elif self.value in ['+', '-', '*', '/', '^']:
            return f'({self.left.to_string()} {self.value} {self.right.to_string()})'
        else:
            return f'{self.value}({self.left.to_string()})'

    def to_sympy(self):
        if self.value in VARIABLES:
            return sp.Symbol(self.value)
        elif isinstance(self.value, (int, float)):
            return sp.Float(self.value)
        elif self.value == '+':
            return self.left.to_sympy() + self.right.to_sympy()
        elif self.value == '-':
            return self.left.to_sympy() - self.right.to_sympy()
        elif self.value == '*':
            return self.left.to_sympy() * self.right.to_sympy()
        elif self.value == '/':
            return self.left.to_sympy() / self.right.to_sympy()
        elif self.value == '^':
            return self.left.to_sympy() ** self.right.to_sympy()
        elif self.value == 'sin':
            return sp.sin(self.left.to_sympy())
        elif self.value == 'cos':
            return sp.cos(self.left.to_sympy())
        elif self.value == 'exp':
            return sp.exp(self.left.to_sympy())
        elif self.value == 'log':
            return sp.log(self.left.to_sympy())
        else:
            raise ValueError(f"Unknown node value: {self.value}")

    def copy(self):
        return Node(self.value,
                    self.left.copy() if self.left else None,
                    self.right.copy() if self.right else None)

    def simplify(self):
        if self.left:
            self.left = self.left.simplify()
        if self.right:
            self.right = self.right.simplify()
        if self.left and self.right and isinstance(self.left.value, (int, float)) and isinstance(self.right.value, (int, float)):
            try:
                return Node(self.evaluate({}))
            except:
                return self
        if self.left and not self.right and isinstance(self.left.value, (int, float)):
            try:
                return Node(self.evaluate({}))
            except:
                return self
        return self

# Tree Utilities
def generate_random_tree(depth=0):
    if depth >= MAX_DEPTH or (depth > 0 and random.random() < 0.3):
        return Node(random.choice(VARIABLES) if random.random() < 0.7 else round(random.uniform(-5, 5), 2))
    op = random.choice(OPERATORS)
    if op in ['+', '-', '*', '/', '^']:
        return Node(op, generate_random_tree(depth + 1), generate_random_tree(depth + 1)).simplify()
    else:
        return Node(op, generate_random_tree(depth + 1)).simplify()

# Gradient utilities
def find_constants(node):
    constants = []
    def recurse(n):
        if isinstance(n.value, (int, float)):
            constants.append(n)
        if n.left:
            recurse(n.left)
        if n.right:
            recurse(n.right)
    recurse(node)
    return constants

def fitness(ind, data):
    errors = []
    for x, y in data:
        try:
            pred = ind.evaluate(x)
            pred = min(max(pred, -1e10), 1e10)
        except:
            pred = 1.0
        errors.append((pred - y) ** 2)
    return sum(errors) / len(errors)

def nudge_constants(node, data, epsilon=1e-4, step=0.1):
    constants = find_constants(node)
    for c in constants:
        orig = c.value
        c.value = orig + epsilon
        err1 = fitness(node, data)
        c.value = orig - epsilon
        err2 = fitness(node, data)
        grad = (err1 - err2) / (2 * epsilon)
        c.value = orig - step * grad
    return node.simplify()

def smart_structure_mutation(node):
    if random.random() < 0.5:
        return Node('+', node.copy(), Node(round(random.uniform(-5, 5), 2))).simplify()
    else:
        return Node(random.choice(['+', '*']), node.copy(), Node(random.choice(VARIABLES))).simplify()

def mutate(node, data=None, depth=0):
    if random.random() < MUTATION_RATE:
        if random.random() < 0.5:
            return generate_random_tree(depth).simplify()
        else:
            return smart_structure_mutation(node)
    new_node = node.copy()
    if data is not None:
        new_node = nudge_constants(new_node, data)
    if new_node.left:
        new_node.left = mutate(new_node.left, data, depth + 1)
    if new_node.right:
        new_node.right = mutate(new_node.right, data, depth + 1)
    return new_node.simplify()

# Crossover
def get_random_node(node):
    nodes = []
    def collect(n):
        if n is None: return
        nodes.append(n)
        if n.left: collect(n.left)
        if n.right: collect(n.right)
    collect(node)
    return random.choice(nodes)

def crossover(t1, t2):
    t1_copy = t1.copy()
    t2_copy = t2.copy()
    n1 = get_random_node(t1_copy)
    n2 = get_random_node(t2_copy)
    n1.value, n1.left, n1.right = n2.value, n2.left, n2.right
    return t1_copy.simplify()

# Selection
def select(population, scores):
    best = None
    for _ in range(TOURNAMENT_SIZE):
        i = random.randint(0, len(population) - 1)
        if best is None or scores[i] < scores[best]:
            best = i
    return population[best]

