def from_sympy(expr):
    if expr.is_Symbol:
        return Node(str(expr))
    elif expr.is_Number:
        return Node(float(expr))
    elif expr.func == sp.Add:
        return Node('+', from_sympy(expr.args[0]), from_sympy(expr.args[1]))
    elif expr.func == sp.Mul:
        return Node('*', from_sympy(expr.args[0]), from_sympy(expr.args[1]))
    elif expr.func == sp.Pow:
        return Node('^', from_sympy(expr.args[0]), from_sympy(expr.args[1]))
    elif expr.func == sp.sin:
        return Node('sin', from_sympy(expr.args[0]))
    elif expr.func == sp.cos:
        return Node('cos', from_sympy(expr.args[0]))
    elif expr.func == sp.exp:
        return Node('exp', from_sympy(expr.args[0]))
    elif expr.func == sp.log:
        return Node('log', from_sympy(expr.args[0]))
    elif expr.func == sp.NegativeOne:
        return Node(-1)
    elif expr.func == sp.Mul and expr.args[0] == -1:
        return Node('*', Node(-1), from_sympy(expr.args[1]))
    elif expr.func == sp.Div:
        return Node('/', from_sympy(expr.args[0]), from_sympy(expr.args[1]))
    else:
        raise ValueError(f"Unsupported SymPy expression: {expr}")

import random

OPERATORS = ['+', '-', '*', '/', '^']
UNARY_FUNCTIONS = ['sin', 'cos', 'log', 'exp']
TERMINALS = list(VARIABLES) + [0.0, 1.0, -1.0]  # Add more constants as needed

def generate_random_tree(depth=0, max_depth=4):
    # Leaf node (terminal or constant)
    if depth >= max_depth or (depth > 0 and random.random() < 0.2):
        terminal = random.choice(TERMINALS + [round(random.uniform(-5.0, 5.0), 2)])
        return Node(terminal)

    node_type = random.choices(
        ['binary', 'unary', 'constant'],
        weights=[0.6, 0.3, 0.1]
    )[0]

    if node_type == 'binary':
        op = random.choice(OPERATORS)
        left = generate_random_tree(depth + 1, max_depth)
        right = generate_random_tree(depth + 1, max_depth)
        return Node(op, left, right)

    elif node_type == 'unary':
        func = random.choice(UNARY_FUNCTIONS)
        child = generate_random_tree(depth + 1, max_depth)
        return Node(func, child)

    else:  # constant node
        return Node(round(random.uniform(-10.0, 10.0), 2))

def mutate(node, max_depth=5, mutation_rate=0.1, current_depth=0):

    if current_depth >= max_depth:
        return node.copy()

    # 1. Decide if we should mutate at this node
    if random.random() < mutation_rate:
        mutation_type = random.choices(
            ['subtree', 'constant_perturb', 'operator_swap', 'variable_swap', 'insert_bias', 'negate_subtree'],
            weights=[0.2, 0.3, 0.2, 0.15, 0.1, 0.05]
        )[0]

        if mutation_type == 'subtree':
            # Replace the entire subtree with a new random one
            return generate_random_tree(depth=current_depth, max_depth=max_depth)

        elif mutation_type == 'constant_perturb' and node.is_constant():
            new_node = node.copy()
            new_node.value += round(random.uniform(-1.0, 1.0), 3)
            return new_node

        elif mutation_type == 'operator_swap' and node.value in ['+', '-', '*', '/', '^']:
            new_node = node.copy()
            new_node.value = random.choice(['+', '-', '*', '/', '^'])
            return new_node

        elif mutation_type == 'variable_swap' and node.value in VARIABLES:
            return Node(random.choice(list(VARIABLES - {node.value})))

        elif mutation_type == 'insert_bias':
            # Add a small constant to the current expression (simulates +b)
            return Node('+', node.copy(), Node(round(random.uniform(-2.0, 2.0), 3)))

        elif mutation_type == 'negate_subtree':
            return Node('*', Node(-1), node.copy())

    # 2. Recurse into children
    new_node = node.copy()
    if new_node.left:
        new_node.left = mutate(new_node.left, max_depth, mutation_rate, current_depth + 1)
    if new_node.right:
        new_node.right = mutate(new_node.right, max_depth, mutation_rate, current_depth + 1)
    return new_node.simplify()


def get_all_subtrees(node, allow_constants=False, min_depth=1):
    subtrees = []

    def collect(n, depth):
        if n is None:
            return
        if depth >= min_depth:
            if allow_constants or not n.is_constant():
                subtrees.append(n)
        if n.left:
            collect(n.left, depth + 1)
        if n.right:
            collect(n.right, depth + 1)

    collect(node, 0)
    return subtrees

def safe_replace(parent, target, replacement):
    """
    Replaces `target` subtree in `parent` with `replacement`.
    """
    if parent is target:
        return replacement.copy()

    new_node = Node(parent.value)
    if parent.left:
        new_node.left = safe_replace(parent.left, target, replacement)
    if parent.right:
        new_node.right = safe_replace(parent.right, target, replacement)
    return new_node

def crossover(parent1, parent2, max_attempts=10):
    for _ in range(max_attempts):
        p1 = parent1.copy()
        p2 = parent2.copy()

        candidates1 = get_all_subtrees(p1, allow_constants=False, min_depth=1)
        candidates2 = get_all_subtrees(p2, allow_constants=False, min_depth=1)

        if not candidates1 or not candidates2:
            continue

        subtree1 = random.choice(candidates1)
        subtree2 = random.choice(candidates2)

        # Avoid swapping constants or flat expressions
        if subtree1.depth() < 1 or subtree2.depth() < 1:
            continue

        # Type check: swap only similar arity
        arity1 = 2 if subtree1.left and subtree1.right else 1 if subtree1.left else 0
        arity2 = 2 if subtree2.left and subtree2.right else 1 if subtree2.left else 0
        if arity1 != arity2:
            continue

        # Perform safe subtree replacement
        new_tree1 = safe_replace(p1, subtree1, subtree2)
        new_tree2 = safe_replace(p2, subtree2, subtree1)
        return new_tree1.simplify(), new_tree2.simplify()

    # Fallback to returning parents if all attempts fail
    return parent1.copy(), parent2.copy()

