def compute_fitness(
    tree,
    X,
    y,
    error_func='mse',
    complexity_penalty=True,
    alpha=0.01,
    complexity_func='depth',
    safe=True
):
    """
    Generalized fitness evaluation for symbolic regression.

    Parameters:
        tree: Symbolic expression tree
        X: Input data
        y: Target output
        error_func: 'mse', 'mae', or custom callable
        complexity_penalty: Apply model complexity penalty
        alpha: Weight for complexity penalty
        complexity_func: 'node_count', 'depth', or custom callable
        safe: Return -inf for invalid trees

    Returns:
        Fitness score (higher is better)
    """
    try:
        y_pred = tree.evaluate(X)
        if safe and (np.any(np.isnan(y_pred)) or np.any(np.isinf(y_pred))):
            return -np.inf
    except:
        return -np.inf

    # Compute error
    if error_func == 'mse':
        error = np.mean((y - y_pred) ** 2)
    elif error_func == 'mae':
        error = np.mean(np.abs(y - y_pred))
    elif callable(error_func):
        error = error_func(y, y_pred)
    else:
        raise ValueError("Unknown error function")

    # Compute complexity penalty
    penalty = 0
    if complexity_penalty:
        if complexity_func == 'node_count':
            penalty = tree.count_nodes()
        elif complexity_func == 'depth':
            penalty = tree.depth()
        elif callable(complexity_func):
            penalty = complexity_func(tree)
        else:
            raise ValueError("Unknown complexity function")

    return -error - alpha * penalty

import numpy as np

def apply_fitness_sharing(
    population,
    fitnesses,
    X,
    similarity_func='structure',  # 'prediction', 'structure', or custom callable
    structure_method='node_overlap',  # 'node_overlap' or other methods
    sigma=0.1,
    alpha=1.0,
    mode='sharing',  # 'sharing' or 'niching'
    normalize=True,
    use_cache=True
):
    """
    Applies fitness sharing or niching to promote population diversity.

    Parameters:
        population: list of trees
        fitnesses: raw fitness scores
        X: input data
        similarity_func: 'prediction', 'structure', or a custom callable
        structure_method: structure similarity measure (e.g., 'node_overlap')
        sigma: bandwidth for similarity decay
        alpha: shape parameter
        mode: 'sharing' (fitness penalty) or 'niching' (niche detection)
        normalize: whether to normalize similarities
        use_cache: avoid duplicate evaluate(X) calls

    Returns:
        Adjusted fitness list (for sharing) or niche densities (for niching)
    """
    n = len(population)
    pred_cache = [None] * n if use_cache else None

    def get_prediction(i):
        if pred_cache[i] is None:
            pred_cache[i] = population[i].evaluate(X)
        return pred_cache[i]

    def prediction_similarity(i, j):
        pred_i = get_prediction(i)
        pred_j = get_prediction(j)
        return np.mean(np.abs(pred_i - pred_j))

    def structure_similarity(i, j):
        sig_i = population[i].get_node_signatures()
        sig_j = population[j].get_node_signatures()
        overlap = len(sig_i & sig_j)
        union = len(sig_i | sig_j)
        if union == 0:
            return 0.0
        return 1.0 - overlap / union  # 0 = same, 1 = disjoint

    if callable(similarity_func):
        similarity = similarity_func
    elif similarity_func == 'prediction':
        similarity = prediction_similarity
    elif similarity_func == 'structure':
        similarity = structure_similarity
    else:
        raise ValueError("Unknown similarity_func")

    adjusted = []
    for i in range(n):
        sharing_sum = 0
        for j in range(n):
            if i == j:
                continue
            sim = similarity(i, j)
            if normalize:
                sim = sim / (sim + 1e-6)  # optional normalization
            sh = max(0, 1 - (sim / sigma) ** alpha)
            sharing_sum += sh

        if mode == 'sharing':
            sharing_factor = max(1, sharing_sum)
            adjusted.append(fitnesses[i] / sharing_factor)
        elif mode == 'niching':
            adjusted.append(sharing_sum)
        else:
            raise ValueError("Unknown mode")

    return adjusted

