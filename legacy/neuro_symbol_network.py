# === Expression Node Classes ===
class Expr:
    def evaluate(self, x): raise NotImplementedError()
    def get_constants(self): return []
    def __str__(self): raise NotImplementedError()

class Const(Expr):
    def __init__(self, value): self.value = value
    def evaluate(self, x): return self.value
    def get_constants(self): return [self]
    def __str__(self): return f"{self.value:.3f}"

class Var(Expr):
    def __init__(self, name): self.name = name
    def evaluate(self, x): return x.get(self.name, 0)
    def __str__(self): return self.name

class Op(Expr):
    def __init__(self, op, children): self.op, self.children = op, children
    def evaluate(self, x):
        vals = [c.evaluate(x) for c in self.children]
        try:
            if self.op == '+': return vals[0] + vals[1]
            if self.op == '-': return vals[0] - vals[1]
            if self.op == '*': return vals[0] * vals[1]
            if self.op == '/': return vals[0] / vals[1] if vals[1] != 0 else 1
            if self.op == '^': return vals[0] ** vals[1] if vals[0] >= 0 else 1
            if self.op == 'sin': return math.sin(vals[0])
            if self.op == 'cos': return math.cos(vals[0])
            if self.op == 'exp': return math.exp(vals[0])
            if self.op == 'log': return math.log(vals[0]) if vals[0] > 0 else 0
        except: return 1
    def get_constants(self):
        consts = []
        for c in self.children:
            consts.extend(c.get_constants())
        return consts
    def __str__(self):
        if len(self.children) == 1:
            return f"{self.op}({self.children[0]})"
        return f"({self.children[0]} {self.op} {self.children[1]})"

# === Utility Functions ===
def safe_eval(expr):
    try: return expr.evaluate({'x': 1})
    except: return 0

def mse_loss(expr, data, target_fn):
    return sum((expr.evaluate({'x': x}) - target_fn(x)) ** 2 for x in data) / len(data)

# === Simplifier ===
def simplify_expr(node):
    if isinstance(node, (Const, Var)): return node
    if isinstance(node, Op):
        node.children = [simplify_expr(child) for child in node.children]

        if node.op == '-' and len(node.children) == 1:
            child = node.children[0]
            if isinstance(child, Const): return Const(-child.value)
            if isinstance(child, Op) and child.op == '-': return child.children[0]

        if all(isinstance(c, Const) for c in node.children):
            return Const(safe_eval(node))

        left, right = node.children if len(node.children) == 2 else (None, None)
        if node.op == '+':
            if isinstance(left, Const) and left.value == 0: return right
            if isinstance(right, Const) and right.value == 0: return left
        if node.op == '*':
            if isinstance(left, Const) and left.value == 1: return right
            if isinstance(right, Const) and right.value == 1: return left
            if isinstance(left, Const) and left.value == 0: return Const(0)
            if isinstance(right, Const) and right.value == 0: return Const(0)
        if node.op == '-':
            if isinstance(right, Const) and right.value == 0: return left
            if isinstance(left, Const) and left.value == 0: return Op('-', [right])
        if node.op == '/':
            if isinstance(right, Const) and right.value == 1: return left
        if node.op == '^':
            if isinstance(right, Const) and right.value == 1: return left
            if isinstance(right, Const) and right.value == 0: return Const(1)
            if isinstance(left, Const) and left.value == 0: return Const(0)

    return node

# === Bias Injector ===
def has_constant(node):
    if isinstance(node, Const): return True
    if isinstance(node, Op): return any(has_constant(c) for c in node.children)
    return False

def inject_bias(node, bias_range=(-1, 1)):
    if not has_constant(node):
        bias = Const(random.uniform(*bias_range))
        return Op('+', [node, bias])
    return node

# === Gradient Constant Tuner ===
def tune_constants(expr, data, target_fn, lr=0.01, steps=10):
    constants = expr.get_constants()
    for _ in range(steps):
        grads = []
        baseline_loss = mse_loss(expr, data, target_fn)
        for const in constants:
            original = const.value
            const.value += 1e-4
            perturbed_loss = mse_loss(expr, data, target_fn)
            grad = (perturbed_loss - baseline_loss) / 1e-4
            grads.append(grad)
            const.value = original
        for i, const in enumerate(constants):
            const.value -= lr * grads[i]
    return expr

