class Node:
    def __init__(self, value, left=None, right=None):
        self.value = value
        self.left = left
        self.right = right

    def evaluate(self, x: Dict[str, float]) -> float:
        try:
            if isinstance(self.value, (int, float)):
                return self.value
            elif self.value in x:
                return x[self.value]
            elif self.value == '+':
                return self.left.evaluate(x) + self.right.evaluate(x)
            elif self.value == '-':
                return self.left.evaluate(x) - self.right.evaluate(x)
            elif self.value == '*':
                return self.left.evaluate(x) * self.right.evaluate(x)
            elif self.value == '/':
                denom = self.right.evaluate(x)
                return self.left.evaluate(x) / denom if abs(denom) > 1e-6 else 1.0
            elif self.value == '^':
                base = self.left.evaluate(x)
                exp = self.right.evaluate(x)
                return math.pow(abs(base), exp)
            elif self.value == 'sin':
                return math.sin(self.left.evaluate(x))
            elif self.value == 'cos':
                return math.cos(self.left.evaluate(x))
            elif self.value == 'exp':
                return math.exp(min(self.left.evaluate(x), 20))
            elif self.value == 'log':
                val = self.left.evaluate(x)
                return math.log(val) if val > 1e-6 else 0.0
        except:
            return 1.0

    def to_string(self):
        if isinstance(self.value, (int, float)) or self.value in ['x0', 'x1']:
            return str(self.value)
        elif self.value in ['+', '-', '*', '/', '^']:
            return f'({self.left.to_string()} {self.value} {self.right.to_string()})'
        else:
            return f'{self.value}({self.left.to_string()})'

    def copy(self):
        return Node(self.value,
                    self.left.copy() if self.left else None,
                    self.right.copy() if self.right else None)

    def simplify(self):
        if self.left:
            self.left = self.left.simplify()
        if self.right:
            self.right = self.right.simplify()
        try:
            if self.left and self.right and all(isinstance(n.value, (int, float)) for n in [self.left, self.right]):
                return Node(self.evaluate({}))
            if self.left and isinstance(self.left.value, (int, float)) and self.value in ['sin', 'cos', 'exp', 'log']:
                return Node(self.evaluate({}))
        except:
            pass
        return self

    def derivative(self, var):
        if isinstance(self.value, (int, float)):
            return Node(0.0)
        elif self.value == var:
            return Node(1.0)
        elif self.value == '+':
            return Node('+', self.left.derivative(var), self.right.derivative(var))
        elif self.value == '-':
            return Node('-', self.left.derivative(var), self.right.derivative(var))
        elif self.value == '*':
            return Node('+', Node('*', self.left.derivative(var), self.right.copy()), Node('*', self.left.copy(), self.right.derivative(var)))
        elif self.value == '/':
            num = Node('-', Node('*', self.left.derivative(var), self.right.copy()), Node('*', self.left.copy(), self.right.derivative(var)))
            den = Node('^', self.right.copy(), Node(2.0))
            return Node('/', num, den)
        elif self.value == '^':
            base, exp = self.left, self.right
            return Node('*', Node('*', Node('^', base.copy(), exp.copy()), Node('log', base.copy())),
                        exp.derivative(var))
        elif self.value == 'sin':
            return Node('*', Node('cos', self.left.copy()), self.left.derivative(var))
        elif self.value == 'cos':
            return Node('*', Node('*', Node(-1.0), Node('sin', self.left.copy())), self.left.derivative(var))
        elif self.value == 'exp':
            return Node('*', Node('exp', self.left.copy()), self.left.derivative(var))
        elif self.value == 'log':
            return Node('/', self.left.derivative(var), self.left.copy())
        return Node(0.0)

