def fitness(ind, data):
    errors = []
    for x, y in data:
        try:
            pred = ind.evaluate(x)
        except:
            pred = 1.0
        errors.append((pred - y) ** 2)
    error = sum(errors) / len(errors)
    return error + LAMBDA_COMPLEXITY * ind.complexity()

def select(pop, scores):
    best = None
    for _ in range(TOURNAMENT_SIZE):
        i = random.randint(0, len(pop) - 1)
        if best is None or scores[i] < scores[best]:
            best = i
    return pop[best]

def crossover(t1, t2):
    def get_nodes(node):
        nodes = []
        def collect(n):
            if n:
                nodes.append(n)
                collect(n.left)
                collect(n.right)
        collect(node)
        return nodes
    t1_copy, t2_copy = t1.copy(), t2.copy()
    n1 = random.choice(get_nodes(t1_copy))
    n2 = random.choice(get_nodes(t2_copy))
    if random.random() < 0.5:
        n1.value, n1.left, n1.right = n2.value, n2.left, n2.right
    return t1_copy.simplify()

def mutate(tree):
    if random.random() < MUTATION_RATE:
        return generate_random_tree()
    tree = tree.copy()
    nodes = []
    def collect(n):
        if n:
            nodes.append(n)
            collect(n.left)
            collect(n.right)
    collect(tree)
    mut_node = random.choice(nodes)
    if random.random() < 0.5:
        new_sub = generate_random_tree()
        mut_node.value, mut_node.left, mut_node.right = new_sub.value, new_sub.left, new_sub.right
    return tree.simplify()

def evolve_symbolic_expression(data, generations=30):
    population = [generate_random_tree() for _ in range(POP_SIZE)]
    for gen in range(generations):
        scores = [fitness(ind, data) for ind in population]
        ranked = sorted(zip(population, scores), key=lambda x: x[1])
        population = [p for p, _ in ranked]
        print(f"Gen {gen+1:03d} | Best Score: {ranked[0][1]:.6f}")
        new_pop = population[:ELITISM]
        while len(new_pop) < POP_SIZE:
            p1 = select(population, scores)
            p2 = select(population, scores)
            child = crossover(p1, p2)
            child = mutate(child)
            new_pop.append(child)
        population = new_pop
    return population[0]

