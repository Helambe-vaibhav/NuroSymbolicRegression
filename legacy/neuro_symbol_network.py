class Node:
    def __init__(self, value, left=None, right=None):
        self.value = value
        self.left = left
        self.right = right

    def evaluate(self, x):
        try:
            if isinstance(self.value, (int, float)):
                return self.value
            elif self.value in x:
                return x[self.value]
            elif self.value == '+':
                return self.left.evaluate(x) + self.right.evaluate(x)
            elif self.value == '-':
                return self.left.evaluate(x) - self.right.evaluate(x)
            elif self.value == '*':
                return self.left.evaluate(x) * self.right.evaluate(x)
            elif self.value == '/':
                denom = self.right.evaluate(x)
                return self.left.evaluate(x) / denom if abs(denom) > 1e-6 else 1.0
            elif self.value == '^':
                base = self.left.evaluate(x)
                exp = self.right.evaluate(x)
                return math.pow(abs(base), exp)
            elif self.value == 'sin':
                return math.sin(self.left.evaluate(x))
            elif self.value == 'cos':
                return math.cos(self.left.evaluate(x))
            elif self.value == 'exp':
                return math.exp(min(self.left.evaluate(x), 20))
            elif self.value == 'log':
                val = self.left.evaluate(x)
                return math.log(val) if val > 1e-6 else 0.0
        except:
            return 1.0

    def to_string(self):
        if isinstance(self.value, (int, float)) or self.value in ['x0', 'x1']:
            return str(self.value)
        elif self.value in ['+', '-', '*', '/', '^']:
            return f'({self.left.to_string()} {self.value} {self.right.to_string()})'
        else:
            return f'{self.value}({self.left.to_string()})'

    def copy(self):
        return Node(self.value,
                    self.left.copy() if self.left else None,
                    self.right.copy() if self.right else None)

    def count_nodes(self):
        count = 1
        if self.left:
            count += self.left.count_nodes()
        if self.right:
            count += self.right.count_nodes()
        return count

    def simplify(self):
        if self.left:
            self.left = self.left.simplify()
        if self.right:
            self.right = self.right.simplify()

        # Rule-based simplification
        if self.value == '+' and self.right and self.right.value == 0:
            return self.left
        if self.value == '+' and self.left and self.left.value == 0:
            return self.right
        if self.value == '*' and self.right and self.right.value == 1:
            return self.left
        if self.value == '*' and self.left and self.left.value == 1:
            return self.right
        if self.value == '*' and ((self.left and self.left.value == 0) or (self.right and self.right.value == 0)):
            return Node(0)

        if self.left and self.right:
            if isinstance(self.left.value, (int, float)) and isinstance(self.right.value, (int, float)):
                try:
                    return Node(self.evaluate({}))
                except:
                    pass
        return self

