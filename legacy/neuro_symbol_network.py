# --- Expression Tree Base ---
class ExprNode(ABC):
    @abstractmethod
    def evaluate(self, x): pass

    @abstractmethod
    def to_string(self): pass

    @abstractmethod
    def simplify(self): pass

    @abstractmethod
    def clone(self): pass

    @abstractmethod
    def get_hash(self): pass

# --- Terminal Node (constants and variables) ---
class TerminalNode(ExprNode):
    def __init__(self, value, is_variable=False):
        self.value = value
        self.is_variable = is_variable

    def evaluate(self, x):
        return x if self.is_variable else self.value

    def to_string(self):
        return 'x' if self.is_variable else str(self.value)

    def simplify(self):
        return self

    def clone(self):
        return TerminalNode(self.value, self.is_variable)

    def get_hash(self):
        return f"VAR" if self.is_variable else f"CONST:{self.value}"

# --- Operator Node (internal function nodes) ---
class OperatorNode(ExprNode):
    def __init__(self, op, children):
        self.op = op
        self.children = children

    def evaluate(self, x):
        args = [c.evaluate(x) for c in self.children]
        try:
            return self.apply_op(args)
        except:
            return 0.0

    def apply_op(self, args):
        if self.op == '+': return args[0] + args[1]
        if self.op == '-': return args[0] - args[1]
        if self.op == '*': return args[0] * args[1]
        if self.op == '/': return safe_div(args[0], args[1])
        if self.op == '^': return args[0] ** args[1]
        if self.op == 'sin': return math.sin(args[0])
        if self.op == 'cos': return math.cos(args[0])
        if self.op == 'log': return safe_log(args[0])
        if self.op == 'exp': return math.exp(args[0])
        return 0.0

    def to_string(self):
        if self.op in ['+', '-', '*', '/', '^']:
            return f"({self.children[0].to_string()} {self.op} {self.children[1].to_string()})"
        else:
            return f"{self.op}({self.children[0].to_string()})"

    def simplify(self):
        simplified_children = [c.simplify() for c in self.children]

        # Constant folding
        if all(isinstance(c, TerminalNode) and not c.is_variable for c in simplified_children):
            val = self.apply_op([c.value for c in simplified_children])
            return TerminalNode(val)

        return OperatorNode(self.op, simplified_children)

    def clone(self):
        return OperatorNode(self.op, [c.clone() for c in self.children])

    def get_hash(self):
        child_hashes = ':'.join([c.get_hash() for c in self.children])
        return f"{self.op}({child_hashes})"

