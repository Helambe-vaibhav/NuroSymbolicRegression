class Node:
    """Represents a node in an expression tree."""
    def __init__(self, value=None, left=None, right=None):
        self.value = value  # Operator (+, -, *, /) or operand (number)
        self.left = left  # Left child (sub-expression)
        self.right = right  # Right child (sub-expression)

    def evaluate(self, inputs):
        """Recursively evaluates the expression tree."""
        if isinstance(self.value, (int, float)):
            return self.value  # Return the number

        if self.left is None or self.right is None:
            return 0  # Invalid case (should not happen)

        left_val = self.left.evaluate(inputs)
        right_val = self.right.evaluate(inputs)

        if self.value == "+":
            return left_val + right_val
        elif self.value == "-":
            return left_val - right_val
        elif self.value == "*":
            return left_val * right_val
        elif self.value == "/" and right_val != 0:  # Avoid division by zero
            return left_val / right_val
        return left_val  # Fallback

    def mutate(self):
        """Mutates a random node in the tree."""
        if random.random() < 0.5:
            self.value = random.choice(operations)  # Change an operation
        else:
            self.left, self.right = self.right, self.left  # Swap children

    def crossover(self, other):
        """Swaps subtrees with another expression."""
        if random.random() < 0.5:
            return Node(self.value, self.left, other.right)
        return Node(other.value, other.left, self.right)

    def __str__(self):
        """Returns the string representation of the tree."""
        if isinstance(self.value, (int, float)):
            return str(self.value)
        return f"({self.left} {self.value} {self.right})"

class ExpressionTree:
    """Represents a full arithmetic expression tree."""
    def __init__(self, depth=3):
        self.root = self.generate_random_tree(depth)

    def generate_random_tree(self, depth):
        """Generates a random expression tree."""
        if depth == 0:
            return Node(random.uniform(-10, 10))  # Random number

        operation = random.choice(operations)
        return Node(operation, self.generate_random_tree(depth - 1), self.generate_random_tree(depth - 1))

    def evaluate(self, inputs):
        """Evaluates the tree with the given inputs."""
        return self.root.evaluate(inputs)

    def mutate(self):
        """Mutates the tree."""
        self.root.mutate()

    def crossover(self, other):
        """Performs crossover with another tree."""
        return ExpressionTree().from_root(self.root.crossover(other.root))

    def from_root(self, root):
        """Creates a tree from a given root node."""
        self.root = root
        return self

    def __str__(self):
        """Returns string representation of the tree."""
        return str(self.root)

class GeneticAlgorithm:
    def __init__(self, num_inputs, population_size=50, mutation_rate=0.3, generations=200, target_type="sum"):
        self.num_inputs = num_inputs
        self.population_size = population_size
        self.mutation_rate = mutation_rate
        self.generations = generations
        self.target_type = target_type

    def fitness(self, expr, inputs):
        """Computes fitness using Mean Squared Error."""
        target_output = TARGET_TYPES[self.target_type](inputs)
        result = expr.evaluate(inputs)
        return -((result - target_output) ** 2)  # Minimize error

    def generate_population(self):
        """Creates an initial population."""
        return [ExpressionTree(depth=3) for _ in range(self.population_size)]

    def select_parents(self, population, inputs):
        """Tournament Selection."""
        tournament_size = 5
        selected = []
        for _ in range(self.population_size // 2):
            competitors = random.sample(population, tournament_size)
            best = max(competitors, key=lambda expr: self.fitness(expr, inputs))
            selected.append(best)
        return selected

    def evolve(self, inputs):
        """Runs the genetic algorithm."""
        population = self.generate_population()

        for generation in range(self.generations):
            selected_parents = self.select_parents(population, inputs)
            new_population = []

            while len(new_population) < self.population_size:
                parent1, parent2 = random.sample(selected_parents, 2)
                child = parent1.crossover(parent2)

                if random.random() < self.mutation_rate:
                    child.mutate()

                new_population.append(child)

            population = new_population

            if generation % 50 == 0:
                best_expr = selected_parents[0]
                print(f"Gen {generation}: Best Expr = {best_expr}")

        return self.select_parents(population, inputs)[0]

# --- 2. NEURAL NETWORK TO OPTIMIZE ARITHMETIC COMBINATIONS ---
class ArithmeticNN(nn.Module):
    def __init__(self):
        super(ArithmeticNN, self).__init__()
        self.weights = nn.Parameter(torch.randn(4))  # 4 weights for +, -, *, /

    def forward(self, x):
        add = x.sum(dim=1) * self.weights[0]
        sub = x.sum(dim=1) * self.weights[1]
        mul = x.prod(dim=1) * self.weights[2]
        div = (x.prod(dim=1) / (x.sum(dim=1) + 1e-6)) * self.weights[3]  # Avoid div by 0
        return add + sub + mul + div

def train_nn(best_expression, inputs, target_type):
    inputs_tensor = torch.tensor(inputs, dtype=torch.float32).unsqueeze(0)  # Convert inputs
    target_output = torch.tensor(TARGET_TYPES[target_type](inputs), dtype=torch.float32)

    model = ArithmeticNN()
    optimizer = optim.Adam(model.parameters(), lr=0.01)
    loss_fn = nn.MSELoss()

    for epoch in range(1000):
        optimizer.zero_grad()
        output = model(inputs_tensor)
        loss = loss_fn(output, target_output)
        loss.backward()
        optimizer.step()

        if epoch % 100 == 0:
            print(f"Epoch {epoch}: Loss = {loss.item()}")

    return model

