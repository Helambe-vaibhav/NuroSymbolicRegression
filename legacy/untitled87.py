class TrainableNeuron:
    def __init__(self, num_inputs=10, learning_rate=0.01):
        self.num_inputs = num_inputs
        # Randomly initialize weights for different operations
        self.weights = {
            "add": np.random.randn(),
            "sub": np.random.randn(),
            "mul": np.random.randn(),
            "div": np.random.randn()
        }
        self.learning_rate = learning_rate

    def forward(self, inputs):
        if len(inputs) != self.num_inputs:
            raise ValueError(f"Expected {self.num_inputs} inputs, but got {len(inputs)}")

        # Compute a weighted sum of different arithmetic operations
        result = (self.weights["add"] * sum(inputs) +
                  self.weights["sub"] * sum(-x for x in inputs) +
                  self.weights["mul"] * np.prod(inputs) +
                  self.weights["div"] * sum(x for x in inputs if x != 0))  # Avoid division by zero

        return result

    def train(self, inputs, desired_output, epochs=1000):
        """Train the neuron to adjust weights based on error."""
        for _ in range(epochs):
            prediction = self.forward(inputs)
            error = prediction - desired_output

            # Gradient update (simple delta rule)
            self.weights["add"] -= self.learning_rate * error
            self.weights["sub"] -= self.learning_rate * error
            self.weights["mul"] -= self.learning_rate * error
            self.weights["div"] -= self.learning_rate * error

    def get_weights(self):
        return self.weights

