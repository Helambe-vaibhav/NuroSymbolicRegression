class Node:
    """Class for a node in the expression tree"""
    def __init__(self, value):
        self.value = value  # Operator or number
        self.left = None
        self.right = None

    def evaluate(self):
        """Recursively evaluate the expression tree"""
        if isinstance(self.value, (int, float)):  # If it's a number, return it
            return self.value

        # Evaluate left and right subtrees
        left_value = self.left.evaluate() if self.left else 0
        right_value = self.right.evaluate() if self.right else 1  # Avoid division by zero

        # Apply the operation
        if self.value == "+":
            return left_value + right_value
        elif self.value == "-":
            return left_value - right_value
        elif self.value == "*":
            return left_value * right_value
        elif self.value == "/" and right_value != 0:
            return left_value / right_value
        return left_value  # Default case (should not happen)

    def __str__(self):
        """Return the string representation of the expression tree"""
        if isinstance(self.value, (int, float)):
            return str(self.value)
        left_str = f"({self.left})" if self.left else ""
        right_str = f"({self.right})" if self.right else ""
        return f"{left_str} {self.value} {right_str}"

class GeneticExpressionTree:
    def __init__(self, num_inputs, population_size=100, mutation_rate=0.2, generations=500):
        self.num_inputs = num_inputs
        self.population_size = population_size
        self.mutation_rate = mutation_rate
        self.generations = generations

    def generate_random_tree(self, inputs):
        """Generate a random expression tree from input values"""
        if len(inputs) == 1:
            return Node(inputs[0])  # Base case: single input

        # Pick a random operator
        operator = random.choice(operations)

        # Split inputs into two groups
        split = random.randint(1, len(inputs) - 1)
        left_inputs = inputs[:split]
        right_inputs = inputs[split:]

        # Create tree nodes
        root = Node(operator)
        root.left = self.generate_random_tree(left_inputs)
        root.right = self.generate_random_tree(right_inputs)

        return root

    def fitness(self, tree, target_output):
        """Compute fitness as inverse of absolute error"""
        result = tree.evaluate()
        error = abs(result - target_output)
        return 1 / (error + 1e-6)  # Avoid division by zero

    def generate_population(self, inputs):
        """Generate an initial random population of expression trees"""
        return [self.generate_random_tree(inputs) for _ in range(self.population_size)]

    def select_parents(self, population, target_output):
        """Select the best individuals based on fitness"""
        scored_population = [(tree, self.fitness(tree, target_output)) for tree in population]
        scored_population.sort(key=lambda x: x[1], reverse=True)  # Sort by highest fitness
        return [tree for tree, _ in scored_population[: self.population_size // 2]]  # Keep top 50%

    def crossover(self, parent1, parent2):
        """Perform crossover by swapping subtrees"""
        if random.random() < 0.5:
            return parent1  # 50% chance to just return the parent unchanged

        new_tree = Node(parent1.value)
        new_tree.left = parent1.left if random.random() < 0.5 else parent2.left
        new_tree.right = parent1.right if random.random() < 0.5 else parent2.right
        return new_tree

    def mutate(self, tree):
        """Randomly mutate an operator or swap a subtree"""
        if random.random() < self.mutation_rate:
            if isinstance(tree.value, str):  # It's an operator, mutate it
                tree.value = random.choice(operations)
            elif tree.left and random.random() < 0.5:
                tree.left = self.mutate(tree.left)
            elif tree.right:
                tree.right = self.mutate(tree.right)
        return tree

    def evolve(self, inputs, target_output):
        """Run the genetic algorithm to find the best expression tree"""
        population = self.generate_population(inputs)

        for generation in range(self.generations):
            selected_parents = self.select_parents(population, target_output)
            new_population = []

            # Create next generation
            while len(new_population) < self.population_size:
                parent1, parent2 = random.sample(selected_parents, 2)
                child = self.crossover(parent1, parent2)
                child = self.mutate(child)
                new_population.append(child)

            population = new_population

            # Display progress every 50 generations
            if generation % 50 == 0:
                best_tree = selected_parents[0]
                best_result = best_tree.evaluate()
                print(f"Gen {generation}: Best Expr = {best_tree}, Output = {best_result}, Target = {target_output}")

        # Return the best expression found
        best_tree = self.select_parents(population, target_output)[0]
        return best_tree

