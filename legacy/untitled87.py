class GeneticNeuron:
    def __init__(self, num_inputs, population_size=100, mutation_rate=0.2, generations=500):
        self.num_inputs = num_inputs
        self.population_size = population_size
        self.mutation_rate = mutation_rate
        self.generations = generations

    def apply_operations(self, inputs, operation_sequence):
        """Apply the arithmetic sequence to the inputs."""
        result = inputs[0]
        for i in range(1, len(inputs)):
            op = operation_sequence[i - 1]
            if op == "+":
                result += inputs[i]
            elif op == "-":
                result -= inputs[i]
            elif op == "*":
                result *= inputs[i]
            elif op == "/" and inputs[i] != 0:
                result /= inputs[i]  # Avoid division by zero
        return result

    def fitness(self, inputs, operation_sequence, target_output):
        """Compute fitness as the inverse of the absolute error."""
        result = self.apply_operations(inputs, operation_sequence)
        error = abs(result - target_output)
        return 1 / (error + 1e-6)  # Avoid division by zero

    def generate_population(self):
        """Generate a random population of operation sequences."""
        return [[random.choice(operations) for _ in range(self.num_inputs - 1)] for _ in range(self.population_size)]

    def select_parents(self, population, inputs, target_output):
        """Select the top individuals based on fitness."""
        scored_population = [(seq, self.fitness(inputs, seq, target_output)) for seq in population]
        scored_population.sort(key=lambda x: x[1], reverse=True)  # Sort by highest fitness
        return [seq for seq, _ in scored_population[: self.population_size // 2]]  # Keep top 50%

    def crossover(self, parent1, parent2):
        """Perform crossover between two parents."""
        crossover_point = random.randint(1, len(parent1) - 1)
        return parent1[:crossover_point] + parent2[crossover_point:]

    def mutate(self, sequence):
        """Randomly mutate an operation in the sequence."""
        if random.random() < self.mutation_rate:
            mutation_idx = random.randint(0, len(sequence) - 1)
            sequence[mutation_idx] = random.choice(operations)
        return sequence

    def evolve(self, inputs, target_output):
        """Run the genetic algorithm to find the best operation sequence."""
        population = self.generate_population()

        for generation in range(self.generations):
            selected_parents = self.select_parents(population, inputs, target_output)
            new_population = []

            # Create next generation
            while len(new_population) < self.population_size:
                parent1, parent2 = random.sample(selected_parents, 2)
                child = self.crossover(parent1, parent2)
                child = self.mutate(child)
                new_population.append(child)

            population = new_population

            # Display progress every 50 generations
            if generation % 50 == 0:
                best_seq = selected_parents[0]
                best_result = self.apply_operations(inputs, best_seq)
                print(f"Gen {generation}: Best Seq = {best_seq}, Output = {best_result}, Target = {target_output}")

        # Return the best sequence found
        best_seq = self.select_parents(population, inputs, target_output)[0]
        return best_seq

