    def differentiate(self, var):
        """Symbolic partial derivative"""
        if self.value == var:
            return Node(1)
        elif self.value in VARIABLES:
            return Node(0)
        elif isinstance(self.value, (int, float)):
            return Node(0)

        u = self.left
        v = self.right

        if self.value == '+':
            return Node('+', u.differentiate(var), v.differentiate(var))
        elif self.value == '-':
            return Node('-', u.differentiate(var), v.differentiate(var))
        elif self.value == '*':
            return Node('+',
                        Node('*', u.differentiate(var), v.copy()),
                        Node('*', u.copy(), v.differentiate(var)))
        elif self.value == '/':
            numerator = Node('-', Node('*', u.differentiate(var), v.copy()),
                                  Node('*', u.copy(), v.differentiate(var)))
            denominator = Node('*', v.copy(), v.copy())
            return Node('/', numerator, denominator)
        elif self.value == 'sin':
            return Node('*', Node('cos', u.copy()), u.differentiate(var))
