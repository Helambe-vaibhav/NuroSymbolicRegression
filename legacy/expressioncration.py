import math
import random
import numpy as np

# Define function set
OPERATORS = ['+', '-', '*', '/', 'sin', 'cos', 'exp', 'log']
VARIABLES = ['x0', 'x1']
MAX_DEPTH = 4
POPULATION_SIZE = 150
GENERATIONS = 150
TOURNAMENT_SIZE = 5
MUTATION_RATE = 0.2
ELITISM = 5
MAX_VAL = 1e6  # clamp values to avoid overflow

# Clamp function
def evaluate_safe(val):
    try:
        if math.isnan(val) or math.isinf(val):
            return 1.0
        return max(-MAX_VAL, min(MAX_VAL, val))
    except:
        return 1.0

# Node definition
class Node:
    def __init__(self, value, left=None, right=None):
        self.value = value
        self.left = left
        self.right = right

    def evaluate(self, x):
        try:
            if self.value in VARIABLES:
                return evaluate_safe(x[self.value])
            elif self.value == '+':
                return evaluate_safe(self.left.evaluate(x) + self.right.evaluate(x))
            elif self.value == '-':
                return evaluate_safe(self.left.evaluate(x) - self.right.evaluate(x))
            elif self.value == '*':
                return evaluate_safe(self.left.evaluate(x) * self.right.evaluate(x))
            elif self.value == '/':
                denominator = self.right.evaluate(x)
                return evaluate_safe(self.left.evaluate(x) / denominator if abs(denominator) > 1e-6 else 1.0)
            elif self.value == 'sin':
                return evaluate_safe(math.sin(self.left.evaluate(x)))
            elif self.value == 'cos':
                return evaluate_safe(math.cos(self.left.evaluate(x)))
            elif self.value == 'exp':
                return evaluate_safe(math.exp(self.left.evaluate(x)))
            elif self.value == 'log':
                val = self.left.evaluate(x)
                return evaluate_safe(math.log1p(abs(val)))
            else:
                return evaluate_safe(float(self.value))
        except:
            return 1.0

    def to_string(self):
        if self.value in VARIABLES or isinstance(self.value, (int, float)):
            return str(self.value)
        elif self.value in ['+', '-', '*', '/']:
            return f'({self.left.to_string()} {self.value} {self.right.to_string()})'
        elif self.value in ['sin', 'cos', 'exp', 'log']:
            return f'{self.value}({self.left.to_string()})'

    def copy(self):
        return Node(self.value,
                    self.left.copy() if self.left else None,
                    self.right.copy() if self.right else None)

# Random expression tree
def generate_random_tree(depth=0):
    if depth >= MAX_DEPTH or (depth > 0 and random.random() < 0.3):
        return Node(random.choice(VARIABLES) if random.random() < 0.7 else round(random.uniform(-5, 5), 2))
    op = random.choice(OPERATORS)
    if op in ['+', '-', '*', '/']:
        return Node(op, generate_random_tree(depth + 1), generate_random_tree(depth + 1))
    else:
        return Node(op, generate_random_tree(depth + 1))

# Mutation
def mutate(node, depth=0):
    if depth >= MAX_DEPTH:
        return node
    if random.random() < MUTATION_RATE:
        return generate_random_tree(depth)
    new_node = node.copy()
    if new_node.left:
        new_node.left = mutate(new_node.left, depth + 1)
    if new_node.right:
        new_node.right = mutate(new_node.right, depth + 1)
    return new_node

# Subtree Crossover
def get_random_node(node):
    nodes = []
    def collect(n):
        if n is None: return
        nodes.append(n)
        if n.left: collect(n.left)
        if n.right: collect(n.right)
    collect(node)
    return random.choice(nodes)

def crossover(t1, t2):
    t1_copy = t1.copy()
    t2_copy = t2.copy()
    n1 = get_random_node(t1_copy)
    n2 = get_random_node(t2_copy)
    n1.value, n1.left, n1.right = n2.value, n2.left, n2.right
    return t1_copy

# Fitness
def fitness(individual, data):
    errors = []
    for x, y in data:
        try:
            pred = individual.evaluate(x)
            error = (pred - y) ** 2
            if math.isnan(error) or math.isinf(error) or error > 1e12:
                error = 1e6  # penalty
        except:
            error = 1e6
        errors.append(error)
    return sum(errors) / len(errors)

# Tournament selection
def select(population, scores):
    best = None
    for _ in range(TOURNAMENT_SIZE):
        i = random.randint(0, len(population) - 1)
        if best is None or scores[i] < scores[best]:
            best = i
    return population[best]

# Generate data
def generate_data():
    data = []
    for _ in range(100):
        x0 = random.uniform(0, 10)
        x1 = random.uniform(0, 10)
        y = math.sin(x0) + math.log1p(x1)
        data.append(({'x0': x0, 'x1': x1}, y))
    return data

# Evolution
def evolve(data):
    population = [generate_random_tree() for _ in range(POPULATION_SIZE)]
    best_fitness = float('inf')
    stagnation = 0

    for gen in range(GENERATIONS):
        scores = [fitness(ind, data) for ind in population]
        ranked = sorted(zip(population, scores), key=lambda x: x[1])
        population = [p for p, _ in ranked]
        scores = [s for _, s in ranked]

        print(f"Gen {gen+1:02d} | Best Score: {scores[0]:.6f} | Expr: {population[0].to_string()}")

        if scores[0] < best_fitness:
            best_fitness = scores[0]
            stagnation = 0
        else:
            stagnation += 1

        if stagnation > 10:
            print("⚠️  Plateau detected: refreshing half of population.")
            population = population[:ELITISM] + [generate_random_tree() for _ in range(POPULATION_SIZE - ELITISM)]
            stagnation = 0
            continue

        new_population = population[:ELITISM]
        while len(new_population) < POPULATION_SIZE:
            parent1 = select(population, scores)
            parent2 = select(population, scores)
            child = crossover(parent1, parent2)
            child = mutate(child)
            new_population.append(child)
        population = new_population

    return population[0]

# Run
if __name__ == "__main__":
    data = generate_data()
    best = evolve(data)
    print("\n✅ Final Best Expression:", best.to_string())

