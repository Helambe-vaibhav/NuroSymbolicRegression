import numpy as np

# Define different types of "operations"
def model_linear(X, w, b):
    return np.dot(X, w) + b

def model_poly(X, w, b):
    return np.dot(X**2, w) + b

def model_interaction(X, w, b):
    return w[0] * X[:, 0] * X[:, 1] + b

# Define training function
def train_model(X, y, model_func, input_size, epochs=500, lr=0.01):
    w = np.random.randn(input_size)
    b = np.random.randn()

    for _ in range(epochs):
        preds = model_func(X, w, b)
        error = preds - y
        loss = np.mean(error**2)

        # Basic gradient estimation (finite differences)
        grad_w = 2 * np.dot(X.T, error) / len(X)
        grad_b = 2 * np.mean(error)

        w -= lr * grad_w
        b -= lr * grad_b

    return w, b, loss

# Try all models
def find_best_model(X, y):
    models = {
        "linear": model_linear,
        "polynomial": model_poly,
        "interaction": model_interaction
    }

    results = {}
    for name, func in models.items():
        w, b, loss = train_model(X, y, func, X.shape[1])
        results[name] = {"weights": w, "bias": b, "loss": loss}

    best = min(results.items(), key=lambda x: x[1]['loss'])
    print(f"Best Model: {best[0]}, Loss: {best[1]['loss']:.4f}")
    return best

